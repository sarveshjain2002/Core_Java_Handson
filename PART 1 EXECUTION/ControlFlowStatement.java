class ControlFlowStatement
{
public static void main(String args[])
{
int a=10, b=15;

if(a>b)
{
System.out.println("A is Greater than B");
}
else if(a>8)
{
System.out.println("Value of A is Greater");
}
else
{
System.out.println("B is Greater than A");
}

}
}